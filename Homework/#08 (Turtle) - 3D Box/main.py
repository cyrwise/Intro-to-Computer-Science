# start settings
import turtle
turtle.speed(0)
#hide turtle
turtle.hideturtle()

# Making the squares
turtle.setheading(-90)
turtle.forward(100)
turtle.setheading(0)
turtle.forward(100)
turtle.setheading(90)
turtle.forward(100)
turtle.setheading(-180)
turtle.forward(200)
turtle.setheading(90)
turtle.forward(100)
turtle.setheading(0)
turtle.forward(100)
turtle.setheading(-90)
turtle.forward(100)

# the middle of the square is = to
# the side * root of 2 = 141.421356237
turtle.setheading(-45)
turtle.forward(141.421356237)
turtle.setheading(135)
turtle.penup()
turtle.forward(141.421356237)
turtle.pendown()
turtle.forward(141.421356237)
turtle.penup
turtle.setheading(-90)
turtle.forward(100)
turtle.pendown
turtle.setheading(-45)
turtle.forward(141.421356237)

#final side
turtle.goto(0,100)
turtle.forward(141.421356237)
